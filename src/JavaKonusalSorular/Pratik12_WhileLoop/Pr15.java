package JavaKonusalSorular.Pratik12_WhileLoop;

public class Pr15 {

    public static void main(String[] args) {
     /*
     Aşağıdaki kod bloğunun çıktısı ne olur?
     */

        int x = 3;
        int y = 0;
        while (y < 3) {
            x += 1;  //x = x + 1
            y += 1;
        }
        System.out.println("x = " + x);
        System.out.println("y = " + y);

    }

}
