package JavaKonusalSorular.Pratik11_ForLoop;

public class Pr09 {

	public static void main(String[] args) {
		/*
		 * 64'ten 0'a kadar cift sayilari yazdirin,
		 * ancak decrement(i--) kullanmayin.
		 * 
		 * OUTPUT :
		 * 64
		 * 62
		 * 60
		 * .
		 * .
		 * 0
		 */

		for (int i = 64; i >=0; i-=2) {
			System.out.print(i+ "\n");
		}

		
	}
		
		
	}


