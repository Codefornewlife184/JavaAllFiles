package JavaKonusalSorular.Pratik03_AritmetikOparatorler;

public class Pr05 {

    public static void main(String[] args) {
        /*
         * Dairenin alanini ve cevresini hesaplayan java kodunu yaziniz.
         *
         * r=7;
         *
         * Pi=3.14
         *
         * Ipucu:
         *
         * Dairenin Cevresi : 2*Pi*r
         *
         * Dairenin Alani : Pi*r*r
         *
         * Ornek Cikti:
         *
         * 43.96
         *
         * 153.86
         */

        double r = 7;
        double pi = 3.14;

        System.out.println("Dairenin Cevresi : "+2 * pi * r); // Dairenin Cevresi : 43.96
        System.out.println("Dairenin Alani : "+pi * r * r); // Dairenin Alani : 153.86

    }

}