package JavaKonusalSorular.Pratik11_ForLoop;

public class Pr20 {

    public static void main(String[] args) {

     // 33-255 e kadar olan harflerin , sayı ve harf değerini konsola yazdırınız.

        for (int i = 33; i <= 255; i++) {
            char harf = (char) i;

            System.out.println(i + " - " + harf);
        }


    }
}


